# arithmetic operators:
x + 10 + 3 * 2
# Use Pemdas. We can always change precedence with parentheses.
x = (10+3) * 2

# Comparison Operators: (we use these to compare values)
x = 3 > 2
print(x)
# also have >=, <, <=, ==, !=
# equality operator: == inequality operator !=

# Logical Operators: (and(both), or(at least one), not(inverses any value that we give it)
price = 25
print(price > 10 and price < 30)
print(price > 10 or price < 30)
print(not price < 30)

